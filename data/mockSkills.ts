// Helper function to generate trend data
function generateTrendData(baseValue: number, volatility: number = 0.1) {
  const data = [];
  const days = 90;
  let currentValue = baseValue;

  for (let i = days; i >= 0; i--) {
    const date = new Date();
    date.setDate(date.getDate() - i);
    const change = (Math.random() - 0.5) * volatility * baseValue;
    currentValue = Math.max(baseValue * 0.7, Math.min(baseValue * 1.3, currentValue + change));

    data.push({
      date: date.toISOString().split('T')[0],
      salary: Math.round(currentValue),
      openings: Math.round(20000 + Math.random() * 30000),
    });
  }

  return data;
}

